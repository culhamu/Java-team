package day04_increment_contenyation;

public class C01_Pre_postIncrement {
    public static void main(String[] args) {
        int sayi=10;
        sayi++;
        System.out.println(sayi);
        int sayi2=45;
        sayi2--;
        System.out.println(sayi2);
        int sayi3=65;
        System.out.println(++sayi3);
        System.out.println(sayi3--);









    }
}
